#!/usr/bin/env node
/**
 * Generates data/fisheries-keys.ts from data/fisheries.ts.
 *
 * Why: the Fishing tab needs to know which rivers have fisheries data so it
 * can hide itself for rivers that don't — but we don't want to import the full
 * ~150 kB fisheries module just to answer that yes/no. This script writes a
 * lightweight Set of river IDs that can be imported cheaply on the server.
 *
 * Run whenever data/fisheries.ts is added to or removed from:
 *   node scripts/gen-fisheries-keys.js
 */

const fs = require('fs')
const path = require('path')

const { FISHERIES } = require('../data/fisheries.ts')

const keys = Object.keys(FISHERIES).sort()

const header = [
  '// AUTO-GENERATED by scripts/gen-fisheries-keys.js',
  '// Re-run whenever data/fisheries.ts is updated.',
  '// Lightweight lookup so UI can show/hide the Fishing tab without importing',
  '// the full fisheries module (~150 kB).',
  '',
].join('\n')

const body = [
  'export const FISHERIES_KEYS = new Set<string>([',
  ...keys.map(k => `  ${JSON.stringify(k)},`),
  '])',
  '',
  'export function hasFisheries(riverId: string): boolean {',
  '  return FISHERIES_KEYS.has(riverId)',
  '}',
  '',
].join('\n')

const outPath = path.join(__dirname, '..', 'data', 'fisheries-keys.ts')
fs.writeFileSync(outPath, header + body)
console.log(`wrote ${keys.length} keys to data/fisheries-keys.ts`)
